const getNavItems = (role: UserRole): NavItem[] => {
  switch (role) {
    case 'admin':
      return adminNavItems;
    case 'faculty':
      return facultyNavItems;
    case 'student':
      return studentNavItems;
    default:
      return [];
  }
};

interface SidebarProps {
  isOpen: boolean;
  onClose: () => void;
}

